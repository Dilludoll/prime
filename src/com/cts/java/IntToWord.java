package com.cts.java;

public class IntToWord {
	public String character(int n) {
		if(n==1) {
			return "one";
		}
		if(n==2) {
			   return "two";
			   
		   }
		if(n==3) {
			   return "three";
			   
		   }
		if(n==4) {
			   return "four";
			   
		   }
		if(n==5) {
			   return "five";
			   
		   }
		if(n==6) {
			   return "six";
		   }
		if(n==7) {
			   return "seven";
			   
		   }
		if(n==8) {
			   return "eight";
			   
		   }
		if(n==9) {
			   return "nine";
			   
		   }
		else {
			return "zero";
		}
	}
	
}
